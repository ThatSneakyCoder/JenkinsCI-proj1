package com.SuperCalculator.CalcProgram.api;

public class CommonUtil {

    /**
     * Returns the sum of the two numbers.
     * @param a
     * @param b
     * @return sum of the parameters.
     */
    public int adder (int a, int b) {
        return a + b;
    }

    /**
     * Returns the difference of the two numbers.
     * @param a
     * @param b
     * @return difference of the parameters.
     */
    public int subtractor (int a, int b) {
        return (b - a);
    }

    /**
     * Returns the quotient of the two numbers.
     * @param a
     * @param b
     * @return quotient of the parameters.
     */
    public int divider (int a, int b) {
        return a / b;
    }

    /**
     * Returns the multiplication of the two numbers.
     * @param a
     * @param b
     * @return multiplication of the parameters.
     */
    public int multiplyer (int a, int b) {
        return a * b;
    }
}
